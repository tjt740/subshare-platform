interface I18nState {
  locale: Locale;          // 生效语言
  pref: LocalePref;        // 用户偏好（auto=跟随地区）
  setPref: (p: LocalePref) => void;
  t: (key: string, vars?: Record<string, string | number>) => string;
  tList: (key: string) => string[];
  geoCountry: string;
}

const Ctx = createContext<I18nState>(null as unknown as I18nState);

function detectFallback(): Locale {
  return /^zh/i.test(navigator.language || '') ? 'zh' : 'en';
}

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [pref, setPrefState] = useState<LocalePref>(
    () => (localStorage.getItem('ss_locale') as LocalePref) || 'auto',
  );
  const [autoLocale, setAutoLocale] = useState<Locale>(detectFallback);
  const [geoCountry, setGeoCountry] = useState('');

  // 跟随 IP/地区：向服务端探测（生产走 CDN 地理头，本地回退 Accept-Language）
  useEffect(() => {
    fetch('/api/geo')
      .then((r) => r.json())
      .then((g) => {
        if (g?.suggestedLocale) setAutoLocale(g.suggestedLocale as Locale);
        if (g?.country) setGeoCountry(g.country);
        // 用户未手动选过地区时，售卖地区也跟随
        if (g?.suggestedRegion && !localStorage.getItem('ss_region_manual')) {
          window.dispatchEvent(
            new CustomEvent('ss-geo-region', { detail: g.suggestedRegion }),
          );
        }
      })
      .catch(() => undefined);
  }, []);

  const setPref = useCallback((p: LocalePref) => {
    setPrefState(p);
    localStorage.setItem('ss_locale', p);
  }, []);

  const locale: Locale = pref === 'auto' ? autoLocale : pref;

  const t = useCallback(
    (key: string, vars?: Record<string, string | number>) => {
      let s = DICT[locale][key] ?? DICT.zh[key] ?? key;
      if (vars) {
        for (const [k, v] of Object.entries(vars)) {
          s = s.split(`{${k}}`).join(String(v));
        }
      }
      return s;
    },
    [locale],
  );
  const tList = useCallback((key: string) => t(key).split('|'), [t]);

  const value = useMemo(
    () => ({ locale, pref, setPref, t, tList, geoCountry }),
    [locale, pref, setPref, t, tList, geoCountry],
  );
  return <Ctx.Provider value={value}>{children}</Ctx.Provider>;
}

export const useI18n = () => useContext(Ctx);
